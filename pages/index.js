function Titulo(props) {
    const Tag = props.tag || 'h1'; /* declarando e instanciando uma constante que recebe o valor da props tag do componente*/
    return (
        <>
            <Tag>{props.children}</Tag> {/* */}
            <style jsx>{`
                ${Tag} {                 /*${Tag} = irá receber o valor Tag e aplicar os recursos logo pode mudar para h1,h3 que irá funcionar */
                    color: ${appConfig.theme.colors.neutrals['000']};                      
                 }
            `}</style>
        </>
    );
}

